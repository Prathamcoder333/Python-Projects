# HI , Here is your code for a simple calculator:-

# Here, "a" refers to first no to be entered and "b" refers to second no to be entered

# Taking input/values from user:-
a = float(input("First no: "))
b = float(input("Second no: "))

# Choosing Operator (+ , -, *, /):-
operator = str(input("Tell your Operator: "))

# Printing result with the help of If statements :-
if operator == "*":
    print(a * b)

if operator == "-":
    print(a - b)

if operator == "+":
    print(a + b)

if operator == "/":
    print(a / b)

if operator == "**":
    print(a ** b)

